/**
 * Represents a custom keyboard input handler.
 * Stores the state of specific keys used in the game.
 */
class CustomKeyboard {
    /** @type {boolean} Indicates if the RIGHT arrow key is pressed */
    RIGHT = false;

    /** @type {boolean} Indicates if the LEFT arrow key is pressed */
    LEFT = false;

    /** @type {boolean} Indicates if the UP arrow key is pressed */
    UP = false;

    /** @type {boolean} Indicates if the DOWN arrow key is pressed */
    DOWN = false;

    /** @type {boolean} Indicates if the SPACE key is pressed (used for jumping) */
    SPACE = false;

    /** @type {boolean} Indicates if the F key is pressed (used for throwing objects) */
    F = false;
}
